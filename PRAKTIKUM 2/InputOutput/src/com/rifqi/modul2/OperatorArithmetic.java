package com.rifqi.modul2;

public class OperatorArithmetic {
    public static void main(String[] args) {
        // operasi pertambahan
        System.out.println("Additional Operation");
        int additionresult = 7+9;
        System.out.println("Result of 7 + 9 = " + additionresult);
        System.out.println();
        // operasi pengurangan
        System.out.println("Reduction Operations");
        int reductionResult = 4 - 1;
        System.out.println("Result of 4 - 1 = " + reductionResult);
        System.out.println();
        // operasi perkalian
        System.out.println("Multiply Operations");
        int multiplyResult = 5 * 5;
        System.out.println("Result of 5 * 5 = " + multiplyResult);
        System.out.println();
        // operasi pembagian
        System.out.println("Divider Operations");
        int DividerResult = 20 / 2;
        System.out.println("Result of 20 / 2 = " + DividerResult);
        System.out.println();
        // operasi modulus atau sisa bagi
        System.out.println("Modulus Operations");
        int modA = 8 % 2;
        System.out.println("Result of 8 % 2 = " + modA);
        System.out.println();
        int modB = 9 % 2;
        System.out.println("Result of 9 % 2 = " + modB);
        System.out.println();
    }
}
