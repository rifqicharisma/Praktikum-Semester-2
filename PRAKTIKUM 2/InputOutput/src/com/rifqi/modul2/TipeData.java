package com.rifqi.modul2;

public class TipeData {
    public static void main(String[] args) {
        // mendeklarasikan byte
        byte a = 10;
        // mendeklarasikan short
        short b = 15000;
        // mendeklarasikan integer
        int c = 150000;
        // mendeklarasikan long
        long d = 150000L;
        // mendeklarasikan float
        float e = 3.5f;
        // mendeklarasikan double
        double f = 5.0;
        // mendeklarasikan boolean
        boolean g = true;
        // mendeklarasikan char
        char item = 'A';

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(item);
    }
}
